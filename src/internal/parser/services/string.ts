export enum CharCode {
	// \n
	lf = 0x0A,
	// \r
	cr = 0x0D,
	// !
	exclamation = 0x21,
	// #
	hash = 0x23,
	// $
	dollar = 0x24,
	// (
	openParen = 0x28,
	// )
	closeParen = 0x29,
	// *
	asterisk = 0x2A,
	// ,
	comma = 0x2C,
	// -
	minus = 0x2D,
	// .
	dot = 0x2E,
	// :
	colon = 0x3A,
	// <
	lessThan = 0x3C,
	// =
	equals = 0x3D,
	// >
	greaterThan = 0x3E,
	// ?
	question = 0x3F,
	// @
	at = 0x40,
	// [
	openBracket = 0x5B,
	// \
	backslash = 0x5C,
	// ]
	closeBracket = 0x5D,
	// _
	underscore = 0x5F,
	// `
	backtick = 0x60,
	// ~
	tilde = 0x7E,
}
